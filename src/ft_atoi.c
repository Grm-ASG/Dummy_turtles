int				ft_atoi(char *str)
{
	int neg;
	int nb;

	neg = 0;
	nb = 0;
	while (*str == ' ' || *str == '\f' || *str == '\n'
	|| *str == '\r' || *str == '\t' || *str == '\v')
		++str;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			neg = 1;
	while (*str >= '0' && *str <= '9')
		nb = nb * 10 + (*str++ - 48);
	if (neg)
		nb *= -1;
	return (nb);
}
